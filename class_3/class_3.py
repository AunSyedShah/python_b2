# arithmetics operators
n1 = 10
n2 = 3
add = n1 + n2
sub = n1 - n2
mul = n1 * n2
div = n1 / n2
mod = n1 % n2
floor_div = n1 // n2
exp = n1 ** n2
print(f"{n1} + {n2} = {add}")
print(f"{n1} - {n2} = {sub}")
print(f"{n1} * {n2} = {mul}")
print(f"{n1} / {n2} = {round(div, 2)}")
print(f"{n1} % {n2} = {mod}")
print(f"{n1} // {n2} = {floor_div}")
print(f"{n1} ** {n2} = {exp}")